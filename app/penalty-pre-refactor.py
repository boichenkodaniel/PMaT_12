def calc(data):
    # Эта функция считает штраф и обновляет статус
    # data[0] - имя, data[1] - дни, data[2] - тип книги
    
    if data[1] > 0:
        if data[2] == "fiction":
            # Считаем штраф для художки
            f = data[1] * 10
            if data[1] > 7:
                print("Просрочка больше недели")
                f = f + 100
            if data[1] > 30:
                print("Просрочка больше месяца")
                f = f + 500
            print("Штраф начислен")
        elif data[2] == "science":
            # Считаем штраф для научной литературы
            f = data[1] * 50
            if data[1] > 7:
                print("Просрочка больше недели")
                f = f + 200
            if data[1] > 30:
                print("Просрочка больше месяца")
                f = f + 1000
            print("Штраф начислен")
        else:
            # Для всех остальных
            f = data[1] * 5
            if data[1] > 7:
                f = f + 50
            print("Штраф начислен")
            
        # Ужасное форматирование и работа со строками
        res = "User " + str(data[0]) + " must pay " + str(f)
        
        # Магические манипуляции с глобальными переменными или имитация записи в файл
        file = open("log.txt", "a")
        file.write(res + "\n")
        file.close()
        
        return f
    else:
        # Если просрочки нет
        return 0